        // Mengatur filter untuk gambar yang tidak aktif saat awal
        document.querySelectorAll('.carousel-inner .carousel-item img').forEach(img => {
            img.style.filter = 'brightness(50%)';
        });

        // Menambahkan event listener untuk mouseover dan mouseout
        document.querySelectorAll('.carousel-inner .carousel-item').forEach(item => {
            item.addEventListener('mouseover', () => {
                item.querySelector('img').style.filter = 'brightness(100%)';
                item.querySelector('.carousel-caption').classList.remove('d-none');
            });
            item.addEventListener('mouseout', () => {
                if (!item.classList.contains('active')) {
                    item.querySelector('img').style.filter = 'brightness(50%)';
                }
                item.querySelector('.carousel-caption').classList.add('d-none');
            });
        });

        // Set filter untuk item yang aktif
        document.querySelectorAll('.carousel-inner .carousel-item.active img').forEach(img => {
            img.style.filter = 'brightness(100%)';
        });
    </script>

